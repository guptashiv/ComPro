#include<iostream>

using namespace std;
#define ul long long int

ul f(ul a, ul b,ul c,ul w,ul x)
{
	if(c<=a)return 0;
	if(b>=x)
	{	if(c-b/x<=a)return c-a;

	else	return(b/x+f(a,b-x*(b/x),c-b/x,w,x));
	}
	else	return 1+f(a-1,b+w-x,c-1,w,x);
}


int main()
{	

	ul a,b,c,w,x;
	cin>>a>>b>>w>>x>>c;
	
	cout<<f(a,b,c,w,x)<<endl;

return 0;
}
